            group, it was
            difficult for Rinpoche to get global awareness within the Buddhist community. As a result, an organization
            was created
            to aid in his global exposure. As well as being a religious concern, we are a non-profit organization
            supported by
            various selflessly ritual people to enhance the holy spirit among the community and to stoke the almighty
            flame of
            religion in the country. This organization was founded in accordance with the holy spirit of Jikdral Thinley
            Rinpoche in
            order to extend the work of peace, tolerance, virtue, and public interest adopted by Lord Buddha.
        </p>

        <p>
            To disseminate the religious quality associated with Rinpoche's significance: for the general good and
            morals. To
